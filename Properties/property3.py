# Operations on Array :
import numpy as np

arr = np.array([10,20,30,40,50,60])

# Addition of 2 to each element : 
print("Addition :", arr + 2)

#Subtraction of each element by 2 :
print("Subtraction :",arr - 2)

#Multiplication of each element by 2 :
print("Multiplication :",arr * 2)

#Division of each element by 2 :
print("Division :",arr / 2)

#Exponential operation 
print("Exponential :",arr ** 2)

#Modulus of each element by 2 : --> remainder
print("Modulus :",arr % 2)
